// Called by Google's reCAPTCHA widget the instant the user checks the box
// (add data-callback="onCareerRecaptchaSuccess" to the .g-recaptcha div in
// people.php so this actually fires - see note below).
function onCareerRecaptchaSuccess() {
    var $form = $("#careerForm");
    if ($form.length) {
        $form.validate().element("[name='g-recaptcha-response']");
    }
}

$(document).ready(function () {

    // Custom rule: only allow pdf/doc/docx extensions
    $.validator.addMethod("resumeExtension", function (value, element) {
        return this.optional(element) || /\.(pdf|doc|docx)$/i.test(value);
    }, "Please upload a PDF, DOC, or DOCX file only.");

    // Custom rule: basic 10 digit mobile number check (adjust as needed)
    $.validator.addMethod("mobileNumber", function (value, element) {
        return this.optional(element) || /^[0-9]{10}$/.test(value);
    }, "Please enter a valid 10-digit mobile number.");

    $("#careerForm").validate({
        // The reCAPTCHA response textarea is hidden (display:none) by Google's
        // widget, and jQuery Validate ignores hidden fields by default - so we
        // explicitly stop ignoring it here to make it a real validated field.
        ignore: ":hidden:not(.g-recaptcha-response)",
        rules: {
            full_name: {
                required: true,
                minlength: 3
            },
            email: {
                required: true,
                email: true
            },
            mobile: {
                required: true,
                mobileNumber: true
            },
            position: {
                required: true
            },
            experience: {
                required: true
            },
            resume: {
                required: true,
                resumeExtension: true
            },
            'g-recaptcha-response': {
                required: true
            }
        },
        messages: {
            full_name: {
                required: "Please enter your full name.",
                minlength: "Name must be at least 3 characters."
            },
            email: {
                required: "Please enter your email address.",
                email: "Please enter a valid email address."
            },
            position: "Please select a position.",
            experience: "Please select your experience.",
            'g-recaptcha-response': "Please complete the reCAPTCHA verification."
        },
        errorElement: "div",
        errorClass: "text-danger small mt-1",
        errorPlacement: function (error, element) {
            if (element.attr("name") === "g-recaptcha-response") {
                error.insertAfter(".g-recaptcha");
            } else {
                error.insertAfter(element);
            }
        },
        highlight: function (element) {
            $(element).addClass("is-invalid");
        },
        unhighlight: function (element) {
            $(element).removeClass("is-invalid");
        },
        submitHandler: function (form) {
            submitCareerForm(form);
            return false; // prevent normal submit, we handle it via AJAX
        }
    });

    function submitCareerForm(form) {
        var $form = $(form);
        var $btn = $("#careerSubmitBtn");
        var formData = new FormData(form);

        $btn.prop("disabled", true).text("Submitting...");

        $.ajax({
            url: "save-application.php",
            type: "POST",
            data: formData,
            contentType: false,
            processData: false,
            dataType: "text" // read as raw text first so a broken JSON response doesn't fail silently
        })
        .done(function (rawResponse) {
            var response;
            try {
                response = JSON.parse(rawResponse);
            } catch (e) {
                alert("Unexpected server response. Please check the browser console (F12) for details.");
                console.error("Server did not return valid JSON:", rawResponse, e);
                return;
            }

            if (response.status === "success") {
                alert(response.message);
                $form[0].reset();
                $form.find(".is-invalid").removeClass("is-invalid");
                if (typeof grecaptcha !== "undefined") {
                    grecaptcha.reset();
                }
                $("#careerModal").modal("hide");
            } else {
                alert(response.message || "Something went wrong. Please try again.");
                if (typeof grecaptcha !== "undefined") {
                    grecaptcha.reset();
                }
            }
        })
        .fail(function (jqXHR, textStatus, errorThrown) {
            console.error("AJAX request failed:", textStatus, errorThrown, jqXHR.responseText);
            alert("Server error. Please try again later.");
            if (typeof grecaptcha !== "undefined") {
                grecaptcha.reset();
            }
        })
        .always(function () {
            $btn.prop("disabled", false).text("Submit Application");
        });
    }

});
</script>






